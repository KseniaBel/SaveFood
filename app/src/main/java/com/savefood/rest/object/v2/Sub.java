
package com.savefood.rest.object.v2;


public class Sub {

    private String label;
    private String tag;
    private String schemaOrgTag;
    private float total;
    private boolean hasRDI;
    private float daily;
    private String unit;

    /**
     * 
     * @return
     *     The label
     */
    public String getLabel() {
        return label;
    }

    /**
     * 
     * @param label
     *     The label
     */
    public void setLabel(String label) {
        this.label = label;
    }

    /**
     * 
     * @return
     *     The tag
     */
    public String getTag() {
        return tag;
    }

    /**
     * 
     * @param tag
     *     The tag
     */
    public void setTag(String tag) {
        this.tag = tag;
    }

    /**
     * 
     * @return
     *     The schemaOrgTag
     */
    public String getSchemaOrgTag() {
        return schemaOrgTag;
    }

    /**
     * 
     * @param schemaOrgTag
     *     The schemaOrgTag
     */
    public void setSchemaOrgTag(String schemaOrgTag) {
        this.schemaOrgTag = schemaOrgTag;
    }

    /**
     * 
     * @return
     *     The total
     */
    public float getTotal() {
        return total;
    }

    /**
     * 
     * @param total
     *     The total
     */
    public void setTotal(float total) {
        this.total = total;
    }

    /**
     * 
     * @return
     *     The hasRDI
     */
    public boolean isHasRDI() {
        return hasRDI;
    }

    /**
     * 
     * @param hasRDI
     *     The hasRDI
     */
    public void setHasRDI(boolean hasRDI) {
        this.hasRDI = hasRDI;
    }

    /**
     * 
     * @return
     *     The daily
     */
    public float getDaily() {
        return daily;
    }

    /**
     * 
     * @param daily
     *     The daily
     */
    public void setDaily(float daily) {
        this.daily = daily;
    }

    /**
     * 
     * @return
     *     The unit
     */
    public String getUnit() {
        return unit;
    }

    /**
     * 
     * @param unit
     *     The unit
     */
    public void setUnit(String unit) {
        this.unit = unit;
    }

}
